# Generate Caddyfile.generated from employee-portal .env (ORIGIN + DRIVE_ORIGIN + optional DOCS_ORIGIN).

$ErrorActionPreference = 'Stop'
$repoRoot = Split-Path -Parent $PSScriptRoot
$envPath = Join-Path $repoRoot '.env'
$outputPath = Join-Path $repoRoot 'Caddyfile.generated'

. (Join-Path $PSScriptRoot 'lib\read-dotenv.ps1')

$envVars = Read-DotEnv $envPath

$portalOrigin = Get-EnvValue $envVars @('ORIGIN', 'PORTAL_ORIGIN')
$driveOrigin = Get-EnvValue $envVars @('DRIVE_ORIGIN')
$docsOrigin = Get-EnvValue $envVars @('DOCS_ORIGIN')
$oaiOrderSenderOrigin = Get-EnvValue $envVars @('OAI_ORDER_SENDER_ORIGIN')
$portalUpstream = Get-EnvValue $envVars @('CADDY_PORTAL_UPSTREAM')
$driveUpstream = Get-EnvValue $envVars @('CADDY_DRIVE_UPSTREAM')
$docsUpstream = Get-EnvValue $envVars @('CADDY_DOCS_UPSTREAM')
$oaiOrderSenderUpstream = Get-EnvValue $envVars @('CADDY_OAI_ORDER_SENDER_UPSTREAM')
$caddyTls = Get-EnvValue $envVars @('CADDY_TLS')

if (-not $portalOrigin) {
	throw 'Set ORIGIN in .env (portal public URL, e.g. http://portal.local.test or https://phh.com).'
}
if (-not $driveOrigin) {
	throw 'Set DRIVE_ORIGIN in .env (drive public URL, e.g. http://drive.local.test or https://drive.phh.com).'
}

if (-not $portalUpstream) { $portalUpstream = 'localhost:1027' }
if (-not $driveUpstream) { $driveUpstream = 'localhost:1025' }
if (-not $docsUpstream) { $docsUpstream = 'localhost:1026' }

$portalBlock = Get-CaddySiteBlock -Origin $portalOrigin -Upstream $portalUpstream -TlsMode $caddyTls
$driveBlock = Get-CaddySiteBlock -Origin $driveOrigin -Upstream $driveUpstream -TlsMode $caddyTls

$docsBlock = ''
if ($docsOrigin) {
	$docsBlock = Get-CaddySiteBlock -Origin $docsOrigin -Upstream $docsUpstream -TlsMode $caddyTls
}

$oaiOrderSenderBlock = ''
if ($oaiOrderSenderOrigin) {
	if (-not $oaiOrderSenderUpstream) { $oaiOrderSenderUpstream = 'localhost:6002' }
	$oaiOrderSenderBlock = Get-CaddySiteBlock -Origin $oaiOrderSenderOrigin -Upstream $oaiOrderSenderUpstream -TlsMode $caddyTls
}

$mariChatbotBlock = ''
if ($mariChatbotOrigin) {
	if (-not $mariChatbotUpstream) { $mariChatbotUpstream = 'localhost:5173' }
	$mariChatbotBlock = Get-CaddySiteBlock -Origin $mariChatbotOrigin -Upstream $mariChatbotUpstream -TlsMode $caddyTls
}

$content = @"
# Generated by scripts/render-caddyfile.ps1 - do not edit by hand.
# Source: .env (ORIGIN, DRIVE_ORIGIN, DOCS_ORIGIN, OAI_ORDER_SENDER_ORIGIN, CADDY_*)

{
	admin off
}

$portalBlock

$driveBlock
"@

if ($docsBlock) {
	$content += "`n`n$docsBlock"
}

if ($oaiOrderSenderBlock) {
	$content += "`n`n$oaiOrderSenderBlock"
}

if ($mariChatbotBlock) {
	$content += "`n`n$mariChatbotBlock"
}

Set-Content -Path $outputPath -Value $content.TrimEnd() -Encoding utf8
Write-Host "Wrote $outputPath"
Write-Host "  Portal: $portalOrigin -> $portalUpstream"
Write-Host "  Drive:  $driveOrigin -> $driveUpstream"
if ($docsOrigin) {
	Write-Host "  Docs:   $docsOrigin -> $docsUpstream"
}
if ($oaiOrderSenderOrigin) {
	Write-Host "  OAI Order Sender: $oaiOrderSenderOrigin -> $oaiOrderSenderUpstream"
}
if ($mariChatbotOrigin) {
	Write-Host "  MariChatbot: $mariChatbotOrigin -> $mariChatbotUpstream"
}
